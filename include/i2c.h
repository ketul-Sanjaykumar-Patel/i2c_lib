/**
 * @file    i2c.h
 * @brief   Universal I2C (Inter-Integrated Circuit) Driver Library
 * @version 2.0.0
 * @date    2024
 *
 * ============================================================================
 *                        I2C PROTOCOL OVERVIEW
 * ============================================================================
 *
 * I2C (Inter-Integrated Circuit), also written I²C or IIC, is a synchronous,
 * multi-master, multi-slave, serial communication bus developed by Philips
 * Semiconductor (now NXP) in 1982 for connecting low-speed peripherals.
 *
 * PHYSICAL BUS:
 *   - SDA  : Serial Data Line  — bidirectional data
 *   - SCL  : Serial Clock Line — generated by master
 *   - Both lines are OPEN-DRAIN and require pull-up resistors to VCC
 *   - Typical pull-up values: 4.7kΩ (standard), 2.2kΩ (fast), 1kΩ (fast+)
 *
 *                VCC       VCC
 *                 |         |
 *               4.7k      4.7k   <-- Pull-up resistors (required!)
 *                 |         |
 *    MASTER ------+---SDA---+------ SLAVE1 --- SLAVE2 --- ...
 *    MASTER ------+---SCL---+------ SLAVE1 --- SLAVE2 --- ...
 *
 * SPEED MODES:
 *   - Standard Mode (Sm)    :   100 kbps
 *   - Fast Mode (Fm)        :   400 kbps
 *   - Fast Mode Plus (Fm+)  :     1 Mbps
 *   - High Speed (Hs)       :   3.4 Mbps
 *   - Ultra Fast (UFm)      :     5 Mbps (unidirectional only)
 *
 * ADDRESSING:
 *   - 7-bit  addressing: up to 112 usable addresses (0x08 – 0x77)
 *   - 10-bit addressing: up to 1008 usable addresses
 *   - Reserved addresses: 0x00 (general call), 0x01–0x07, 0x78–0x7F
 *
 * ============================================================================
 *                        TRANSACTION FORMAT
 * ============================================================================
 *
 * WRITE transaction:
 *   START | ADDR[7:1] W(0) | ACK | DATA[0] | ACK | DATA[1] | ACK | ... | STOP
 *
 * READ transaction:
 *   START | ADDR[7:1] R(1) | ACK | DATA[0] | ACK | DATA[1] | NACK | STOP
 *
 * REPEATED START (combined read/write without releasing the bus):
 *   START | ADDR W | ACK | REG | ACK | RSTART | ADDR R | ACK | DATA | NACK | STOP
 *
 * BIT TIMING:
 *   - Data valid during HIGH SCL
 *   - SDA may change only when SCL is LOW
 *   - START : SDA HIGH→LOW while SCL HIGH
 *   - STOP  : SDA LOW→HIGH while SCL HIGH
 *
 * ============================================================================
 *                        REAL-WORLD USAGE (2024)
 * ============================================================================
 *
 * I2C is used everywhere low-bandwidth peripheral communication is needed:
 *
 *  [SENSORS & ENVIRONMENT]
 *   - BME280 / BME680   : Temperature, Humidity, Pressure (IoT, Weather stations)
 *   - MPU-6050 / ICM-42688 : 6-axis IMU (Drones, Wearables, VR headsets)
 *   - BMP390            : Barometric pressure (Altimeters, Smartphones)
 *   - SHT31 / HDC1080   : Humidity/Temperature (Industrial HVAC)
 *   - VEML7700 / BH1750 : Ambient light (Auto display brightness)
 *   - VL53L1X           : Time-of-Flight distance (Robots, Phones)
 *   - ADS1115           : 16-bit ADC (Precision data acquisition)
 *   - INA226 / INA3221  : Current/Power monitor (Battery management)
 *   - MAX30105           : Pulse oximeter (Medical wearables)
 *   - MLX90614           : IR thermometer (Contactless medical, automotive)
 *
 *  [DISPLAYS & OUTPUT]
 *   - SSD1306 / SSD1315 : OLED display (Smart watches, IoT displays)
 *   - SH1106            : OLED 128×64 (DIY projects, hobbyist boards)
 *   - PCA9685           : 16-ch PWM driver (Servo robots, LED matrices)
 *   - IS31FL3741        : LED matrix driver (Keyboards, wearables)
 *   - HT16K33           : LED matrix driver (7-segment displays)
 *
 *  [MEMORY & STORAGE]
 *   - AT24CXX / M24CXX  : EEPROM (Configuration storage, calibration data)
 *   - M95XXX            : FRAM (Industrial logging)
 *
 *  [POWER MANAGEMENT]
 *   - BQ25895 / BQ24074 : Battery charger IC (Smartphones, laptops)
 *   - MAX17048           : Fuel gauge (Battery % estimation)
 *   - TPS65090           : Power management (Chromebooks, embedded Linux)
 *   - LP8755            : Multi-phase buck converter (Qualcomm SoCs)
 *
 *  [CONNECTIVITY & GPIO EXPANSION]
 *   - PCF8574 / MCP23017 : GPIO expander (When MCU pins are limited)
 *   - PCA9555            : 16-bit I/O expander (Industrial PLCs)
 *   - TCA9548A           : 1-to-8 I2C multiplexer (Multiple same-address sensors)
 *
 *  [CLOCK & TIMING]
 *   - DS3231 / PCF8563  : Real-time clock (Data loggers, embedded Linux boards)
 *   - DS1307            : RTC (Arduino projects, industrial timers)
 *
 *  [AUDIO]
 *   - CS43L22           : DAC/amplifier (STM32 Discovery boards, audio players)
 *   - MAX9867           : Audio codec (Portable audio devices)
 *   - TAS5825M          : Class-D amplifier (Smart speakers)
 *
 *  [AUTOMOTIVE (AEC-Q100 grade devices)]
 *   - TJA1101           : Ethernet PHY (Automotive ADAS)
 *   - PCA9615           : Differential I2C bus buffer (Long-distance automotive bus)
 *   - MCP4716           : DAC (Engine control units)
 *
 *  [INDUSTRIAL & MEDICAL]
 *   - ADT7420           : High-precision temp (Medical equipment)
 *   - LIS2DH12          : Accelerometer (Industrial vibration monitoring)
 *   - SFM3000           : Mass flow meter (Medical ventilators)
 *
 *  [CONSUMER ELECTRONICS]
 *   - Smartphones use 20–50+ I2C devices per device
 *   - Laptops use I2C for batteries (SMBus = I2C subset), keyboard, touchpad
 *   - Smart TVs use I2C for tuners, HDCP, EDID (monitor capabilities)
 *   - DDC/CI monitor control protocol is I2C over HDMI/DisplayPort
 *
 * ============================================================================
 *                        SMBus vs I2C
 * ============================================================================
 *
 * SMBus (System Management Bus) is a subset of I2C, used in:
 *   - Laptop battery management (BQ40Z50, etc.)
 *   - Server BMC (Board Management Controllers)
 *   - PC power supplies (PMBus)
 *   - DDR memory SPD (Serial Presence Detect)
 * Differences: stricter timing, 100kHz max, mandatory ACK timeout, PEC checksum
 *
 * ============================================================================
 *                        THIS LIBRARY FEATURES
 * ============================================================================
 *
 *  [CORE API]
 *   - Software (bit-bang) I2C: works on any GPIO-capable MCU
 *   - Hardware I2C: HAL abstraction layer for STM32, AVR, ESP32, RP2040
 *   - Master write, read, combined write-then-read (register access)
 *   - 7-bit and 10-bit addressing support
 *   - Repeated START support
 *
 *  [ADVANCED FEATURES]
 *   - Multi-master arbitration detection
 *   - Clock stretching support
 *   - Bus recovery (9-clock pulse sequence per I2C spec §3.1.16)
 *   - DMA transfer support (hardware I2C)
 *   - Transaction queue / async API
 *   - Timeout protection on every operation
 *
 *  [DIAGNOSTICS]
 *   - Bus scan: detect all devices on the bus
 *   - Error codes with descriptive messages
 *   - Bus state machine: IDLE, BUSY, ERROR states
 *   - Statistics: transaction count, error count, bus load
 *
 *  [PLATFORM SUPPORT]
 *   - STM32 (HAL-based, all series)
 *   - AVR / Arduino (Wire.h compatible)
 *   - ESP32 / ESP-IDF
 *   - Raspberry Pi / Linux (via /dev/i2c-N)
 *   - Bare-metal / RTOS (FreeRTOS mutex included)
 *   - Portable software I2C fallback (any platform with GPIO)
 *
 * @author  I2C Library Contributors
 * @license MIT License
 */

